/**
 * add - adds two numbers
 * @a: first number
 * @b: second number
 * Return: the sum of the two numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts a nnumber from a anumber
 * @a: the first number
 * @b: the second number
 * Return: the difference between the two numbers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - divides a number by a number
 * @a: the first number
 * @b: the second number
 * Return: the division of two numbers
 */

/**
 * mul - multiplies two nmbers
 * @a: the first number
 * @b: the second number
 * Return: the multiplicaton of the two numbers
 */
int mul(int a, int b)
{
	return (a * b);
}

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - finds the modulo of a number
 * @a: the first number
 * @b: the second number
 * Return: the mod of the two numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
